create table public.app_order_items (
  id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  fk_app_orders_id bigint null,
  fk_app_products_id bigint null,
  "bb_TransactionId" text null,
  "bb_Quantity" smallint null,
  "bb_TotalPrice" numeric null,
  "bb_TaxAmount" numeric null,
  "bb_TaxIndex" smallint null,
  "bb_Dicount" numeric null,
  "bb_GetPriceFromArticleIfAny" boolean null,
  "bb_IsCoupon" boolean null,
  "bb_ShippingProfileId" smallint null,
  "bb_DontAdjustStock" boolean null,
  "bb_UnrebatedTotalPrice" numeric null,
  "bb_SerialNumber" text null,
  "bb_InvoiceSKU" text null,
  "bb_StockId" bigint null,
  constraint app_order_items_pkey primary key (id),
  constraint app_order_items_fk_app_orders_id_fkey foreign KEY (fk_app_orders_id) references app_orders (id) on delete CASCADE,
  constraint app_order_items_fk_app_products_id_fkey foreign KEY (fk_app_products_id) references app_products (id)
) TABLESPACE pg_default;